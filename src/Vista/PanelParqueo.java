    public void getdataPark() {
        String tablename = "vwEstacionamientos";
        if (tbpark.getRowCount() > 0) {//si hay columnas antes de cargar 
            tbpark.removeRow(0);      //borre las filas q ten√≠a 
        } else {//si no hay filas
            try {
                ResultSet rs = Controlador.ControllerAccesos.getdata(tablename);
                while (rs.next()) {
                    Object[] data = {rs.getInt("idEstacionamiento"), rs.getInt("numero_estacionamiento"), rs.getDate("fecha"), rs.getInt("idAcceso"), rs.getTime("hora"), rs.getInt("idParqueo"), rs.getString("Carnet"), rs.getInt("idVehiculo"), rs.getString("nombre_p"), rs.getString("apellido_p"), rs.getString("placa"), rs.getString("Ubicacion"), rs.getInt("numero_parqueo"), btnupdate, btndelete};
                    tbpark.addRow(data);
                    //que recupere los datos q especifico con la columname y los guarde en un object
                }
            } catch (SQLException e) {
                System.out.println("Error view: " + e.toString());
            }

        }
    }

    public void deletePark(int ID) {
        ControllerParqueo park = new ControllerParqueo();
        park.setID(ID);
        if (park.getID() > 0) {
            if (park.deletePark() == true) {
                getdataPark();
            } else {
                JOptionPane.showMessageDialog(null, "No se pudo eliminar el registro", "Error", JOptionPane.WARNING_MESSAGE);
            }
        }

    }

